using System;

namespace MyMath
{
    /// <summary>
    /// This is my Operations class.
    /// </summary>
    public class Operations
    {
        /// <summary>
        /// Adds two integers.
        /// </summary>
        /// <param name="a">First integer value</param>
        /// <param name="b">Second integer value</param>
        /// <returns>The sum of int a and int b</returns>
        public static int Add(int a, int b)
        {
            return a + b;
        }
    }
}
